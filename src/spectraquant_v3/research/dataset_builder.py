"""Research dataset builder for SpectraQuant-AI-V3.

Transforms OHLCV + feature DataFrames into labeled research datasets by:

1. Computing forward-return labels (1-day, N-day) for each symbol.
2. Merging all per-symbol feature DataFrames into a single cross-sectional
   panel with a ``(date, symbol)`` MultiIndex.
3. Splitting the panel into train / validation / test partitions by date.
4. Persisting train/val/test parquet files to a configurable output directory.

The builder is asset-class agnostic — it accepts the ``feature_map`` produced
by either :class:`~spectraquant_v3.crypto.features.engine.CryptoFeatureEngine`
or :class:`~spectraquant_v3.equities.features.engine.EquityFeatureEngine`.

Usage example::

    from spectraquant_v3.research.dataset_builder import ResearchDatasetBuilder

    builder = ResearchDatasetBuilder(output_dir="/tmp/research")
    result = builder.build(
        feature_map={"BTC": df_btc, "ETH": df_eth},
        forward_windows=[1, 5],
        train_frac=0.70,
        val_frac=0.15,
    )
    # result.train_path, result.val_path, result.test_path are written parquets
    print(result.summary())
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import numpy as np
import pandas as pd

from spectraquant_v3.core.errors import DataSchemaError, EmptyPriceDataError

logger = logging.getLogger(__name__)

# Feature columns generated by the feature engines

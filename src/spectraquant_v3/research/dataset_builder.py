# ---------------------------------------------------------------------------
# Result dataclass
# ---------------------------------------------------------------------------


@dataclass
class DatasetBuildResult:
    """Summary of a completed dataset build.

    Attributes:
        output_dir:        Directory where artefacts were written.
        train_path:        Parquet file containing the training partition.
        val_path:          Parquet file containing the validation partition,
                           or ``None`` when ``val_frac=0``.
        test_path:         Parquet file containing the test partition.
        manifest_path:     JSON manifest describing the build.
        total_rows:        Total rows in the combined panel.
        n_symbols:         Number of unique symbols in the panel.
        n_dates:           Number of unique dates in the panel.
        train_rows:        Rows in the training partition.
        val_rows:          Rows in the validation partition.
        test_rows:         Rows in the test partition.
        feature_columns:   List of input feature column names.
        label_columns:     List of forward-return label column names.
        generated_at:      ISO-8601 timestamp of when the build completed.
        extra:             Optional arbitrary metadata dict.
    """

    output_dir: str
    train_path: str
    val_path: str | None
    test_path: str
    manifest_path: str
    total_rows: int
    n_symbols: int
    n_dates: int
    train_rows: int
    val_rows: int
    test_rows: int
    feature_columns: list[str] = field(default_factory=list)
    label_columns: list[str] = field(default_factory=list)
    generated_at: str = ""
    extra: dict[str, Any] = field(default_factory=dict)

    def summary(self) -> str:
        """Return a human-readable one-paragraph summary of the build."""
        return (
            f"ResearchDataset built at {self.generated_at}\n"
            f"  symbols={self.n_symbols}  dates={self.n_dates}  "
            f"total_rows={self.total_rows}\n"
            f"  train={self.train_rows}  val={self.val_rows}  "
            f"test={self.test_rows}\n"
            f"  features={len(self.feature_columns)}  "
            f"labels={len(self.label_columns)}\n"
            f"  output_dir={self.output_dir}"
        )

    def to_dict(self) -> dict[str, Any]:
        """Serialise to a plain dict (JSON-compatible)."""
        return {
            "output_dir": self.output_dir,
            "train_path": self.train_path,
            "val_path": self.val_path,
            "test_path": self.test_path,
            "manifest_path": self.manifest_path,
            "total_rows": self.total_rows,
            "n_symbols": self.n_symbols,
            "n_dates": self.n_dates,
            "train_rows": self.train_rows,
            "val_rows": self.val_rows,
            "test_rows": self.test_rows,
            "feature_columns": self.feature_columns,
            "label_columns": self.label_columns,
            "generated_at": self.generated_at,
            "extra": self.extra,
        }


# ---------------------------------------------------------------------------
# Builder
# ---------------------------------------------------------------------------


class ResearchDatasetBuilder:
    """Build a labeled cross-sectional research dataset from per-symbol features.

    Args:
        output_dir:    Directory where parquet + manifest files are written.
                       Created if it does not exist.
        run_id:        Optional run identifier used in output filenames.
        dropna:        When ``True`` (default), drop rows where any feature
                       or label column is NaN.
    """

    def __init__(
        self,
        output_dir: str | Path,
        run_id: str = "research",
        dropna: bool = True,
    ) -> None:
        self.output_dir = Path(output_dir)
        self.run_id = run_id
        self.dropna = dropna

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def build(
        self,
        feature_map: dict[str, pd.DataFrame],
        forward_windows: list[int] | None = None,
        train_frac: float = 0.70,
        val_frac: float = 0.15,
        extra: dict[str, Any] | None = None,
    ) -> DatasetBuildResult:
        """Build and persist a labeled research dataset.

        Args:
            feature_map:      Dict mapping canonical symbol → enriched OHLCV
                              DataFrame (output of a feature engine).
            forward_windows:  List of forward-return horizons in days, e.g.
                              ``[1, 5, 20]``.  Labels are added as
                              ``fwd_ret_<N>d`` columns.  Defaults to ``[1, 5]``.
            train_frac:       Fraction of *dates* to use for training.
            val_frac:         Fraction of *dates* for validation (``0`` to skip).
            extra:            Optional metadata to embed in the manifest.

        Returns:
            :class:`DatasetBuildResult` with paths to written artefacts.

        Raises:
            EmptyPriceDataError: If ``feature_map`` is empty.
            DataSchemaError:     If any per-symbol DataFrame is not a valid
                                 DataFrame.
            ValueError:          If ``train_frac + val_frac >= 1.0``.
        """
        if not feature_map:
            raise EmptyPriceDataError(
                "ResearchDatasetBuilder.build: feature_map is empty. "
                "Run the feature engine first."
            )

        if train_frac + val_frac >= 1.0:
            raise ValueError(
                f"train_frac ({train_frac}) + val_frac ({val_frac}) must be < 1.0 "
                "so that the test partition is non-empty."
            )

        if forward_windows is None:
            forward_windows = [1, 5]

        # ------------------------------------------------------------------
        # 1. Label each per-symbol DataFrame
        # ------------------------------------------------------------------
        labeled: dict[str, pd.DataFrame] = {}
        for sym, df in feature_map.items():
            if not isinstance(df, pd.DataFrame):
                raise DataSchemaError(
                    f"ResearchDatasetBuilder: expected DataFrame for '{sym}', "
                    f"got {type(df).__name__}."
                )
            labeled[sym] = self._add_labels(df, forward_windows=forward_windows)

        # ------------------------------------------------------------------
        # 2. Build cross-sectional panel
        # ------------------------------------------------------------------
        panel = self._build_panel(labeled)

        if panel.empty:
            raise EmptyPriceDataError(
                "ResearchDatasetBuilder: panel is empty after labeling and NaN "
                "removal.  Check that your feature DataFrames have enough rows."
            )

        # ------------------------------------------------------------------
        # 3. Identify feature / label columns
        # ------------------------------------------------------------------
        label_cols = [f"fwd_ret_{w}d" for w in forward_windows]
        feat_cols = [c for c in panel.columns if c not in label_cols and c != "symbol"]

        # ------------------------------------------------------------------
        # 4. Time-based train / val / test split
        # ------------------------------------------------------------------
        dates = sorted(panel.index.get_level_values("date").unique())
        n_dates = len(dates)

        n_train = max(1, int(n_dates * train_frac))
        n_val = int(n_dates * val_frac)
        n_test = n_dates - n_train - n_val
        if n_test <= 0:
            # Ensure at least one test date
            n_train = max(1, n_train - 1)
            n_test = n_dates - n_train - n_val

        train_dates = set(dates[:n_train])
        val_dates = set(dates[n_train: n_train + n_val])
        test_dates = set(dates[n_train + n_val:])

        all_dates = panel.index.get_level_values("date")
        train_df = panel.loc[all_dates.isin(train_dates)]
        val_df = panel.loc[all_dates.isin(val_dates)] if n_val > 0 else pd.DataFrame()
        test_df = panel.loc[all_dates.isin(test_dates)]

        # ------------------------------------------------------------------
        # 5. Write parquet artefacts
        # ------------------------------------------------------------------
        self.output_dir.mkdir(parents=True, exist_ok=True)

        train_path = self.output_dir / f"train_{self.run_id}.parquet"
        val_path = self.output_dir / f"val_{self.run_id}.parquet" if n_val > 0 else None
        test_path = self.output_dir / f"test_{self.run_id}.parquet"

        train_df.to_parquet(train_path)
        if val_path is not None:
            val_df.to_parquet(val_path)
        test_df.to_parquet(test_path)

        # ------------------------------------------------------------------
        # 6. Write manifest
        # ------------------------------------------------------------------
        generated_at = datetime.now(timezone.utc).isoformat()
        result = DatasetBuildResult(
            output_dir=str(self.output_dir),
            train_path=str(train_path),
            val_path=str(val_path) if val_path is not None else None,
            test_path=str(test_path),
            manifest_path="",  # filled below
            total_rows=len(panel),
            n_symbols=len(feature_map),
            n_dates=n_dates,
            train_rows=len(train_df),
            val_rows=len(val_df),
            test_rows=len(test_df),
            feature_columns=list(feat_cols),
            label_columns=label_cols,
            generated_at=generated_at,
            extra=extra or {},
        )

        manifest_path = self.output_dir / f"dataset_manifest_{self.run_id}.json"
        manifest_path.write_text(json.dumps(result.to_dict(), indent=2))
        result.manifest_path = str(manifest_path)

        logger.info(
            "ResearchDatasetBuilder: dataset written to %s  "
            "(train=%d val=%d test=%d rows)",
            self.output_dir,
            result.train_rows,
            result.val_rows,
            result.test_rows,
        )

        return result

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _add_labels(
        self,
        df: pd.DataFrame,
        forward_windows: list[int],
    ) -> pd.DataFrame:
        """Add forward-return label columns to *df*.

        For each horizon *N* in *forward_windows*, compute the N-day
        forward log-return: ``ln(close[t+N] / close[t])`` and add it as
        the column ``fwd_ret_<N>d``.

        Args:
            df:              DataFrame with at minimum a ``close`` column.
            forward_windows: Horizons to compute.

        Returns:
            Copy of *df* with ``fwd_ret_<N>d`` columns appended.  Rows
            that are NaN in any label column are kept — the caller's
            ``dropna`` flag controls removal at the panel level.
        """
        out = df.copy()
        # Normalise column names
        out.columns = [c.lower() for c in out.columns]

        if "close" not in out.columns:
            raise DataSchemaError(
                f"ResearchDatasetBuilder._add_labels: DataFrame is missing the "
                "'close' column required for label computation."
            )

        close = out["close"].astype(float)
        for w in forward_windows:
            # shift(-w) brings the future close back to the current row
            out[f"fwd_ret_{w}d"] = np.log(close.shift(-w) / close)

        return out

    def _build_panel(
        self,
        labeled: dict[str, pd.DataFrame],
    ) -> pd.DataFrame:
        """Concatenate per-symbol DataFrames into a cross-sectional panel.

        The resulting DataFrame has a two-level MultiIndex ``(date, symbol)``
        and contains all common columns across symbols.

        Args:
            labeled: Dict mapping canonical symbol → labeled DataFrame.

        Returns:
            Panel DataFrame.  When :attr:`dropna` is ``True`` (default),
            rows with any NaN in feature or label columns are removed.
        """
        frames: list[pd.DataFrame] = []
        for sym, df in labeled.items():
            tmp = df.copy()
            tmp["symbol"] = sym
            tmp.index.name = "date"
            frames.append(tmp)

        if not frames:
            return pd.DataFrame()

        panel = pd.concat(frames, axis=0)
        panel = panel.reset_index().rename(columns={"index": "date"})

        # Handle DatetimeIndex that may already be named "date"
        if "date" not in panel.columns and panel.index.name == "date":
            panel = panel.reset_index()

        panel = panel.set_index(["date", "symbol"])
        panel = panel.sort_index()

        if self.dropna:
            # Keep rows when optional long-lookback features (e.g. MA-200 derived
            # columns) are missing, but enforce non-null labels and core OHLCV
            # fields so downstream training data remains well-formed.
            label_cols = [c for c in panel.columns if c.startswith("fwd_ret_")]
            required_cols = [
                c
                for c in ["open", "high", "low", "close", "volume", *label_cols]
                if c in panel.columns
            ]
            if required_cols:
                panel = panel.dropna(subset=required_cols)

        return panel
